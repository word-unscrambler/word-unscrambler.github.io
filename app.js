// Setup event listeners
function setupEventListeners() {
  // Input events
  scrambledInput.addEventListener("input", handleInput)
  scrambledInput.addEventListener("keydown", (e) => {
    if (e.key === "Enter") {
      e.preventDefault()
      unscramble()
    }
  })

  // Button events
  clearBtn.addEventListener("click", clearInput)
  unscrambleBtn.addEventListener("click", unscramble)
  copyAllBtn.addEventListener("click", copyAllWords)

  // Filter events
  lengthFilter.addEventListener("change", filterResults)
  containsFilter.addEventListener("input", filterResults)

  // Theme toggle
  themeToggle.addEventListener("click", toggleTheme)

  // Mobile menu
  mobileMenuToggle.addEventListener("click", toggleMobileMenu)

  // Subscribe form
  subscribeForm.addEventListener("submit", handleSubscribe)

  // Close mobile menu on link click
  document.querySelectorAll(".mobile-nav-link").forEach((link) => {
    link.addEventListener("click", () => {
      mobileNav.classList.remove("active")
    })
  })
}

// Handle input with debounce
function handleInput() {
  clearTimeout(debounceTimer)
  debounceTimer = setTimeout(() => {
    if (scrambledInput.value.trim().length >= 2) {
      unscramble()
    } else {
      showEmptyState()
    }
  }, 300)
}

// Clear input
function clearInput() {
  scrambledInput.value = ""
  lengthFilter.value = "all"
  containsFilter.value = ""
  showEmptyState()
  scrambledInput.focus()
}

// Main unscramble function
async function unscramble() {
  const input = scrambledInput.value
    .trim()
    .toLowerCase()
    .replace(/[^a-z]/g, "")

  if (input.length < 2) {
    showEmptyState()
    return
  }

  showLoading()

  // Use setTimeout to allow UI to update
  await new Promise((resolve) => setTimeout(resolve, 50))

  const results = findWords(input)

  hideLoading()

  if (results.length === 0) {
    showNoResults()
    return
  }

  displayResults(results)
}

// Find all valid words from scrambled letters
function findWords(letters) {
  const letterCounts = getLetterCounts(letters)
  const results = []

  for (const word of wordList) {
    if (word.length > letters.length) continue
    if (canFormWord(word, letterCounts)) {
      results.push(word)
    }
  }

  // Sort by length (descending) then alphabetically
  results.sort((a, b) => {
    if (b.length !== a.length) return b.length - a.length
    return a.localeCompare(b)
  })

  return results
}

// Get letter frequency count
function getLetterCounts(str) {
  const counts = {}
  for (const char of str) {
    counts[char] = (counts[char] || 0) + 1
  }
  return counts
}

// Check if word can be formed from available letters
function canFormWord(word, availableLetters) {
  const wordCounts = getLetterCounts(word)
  for (const [letter, count] of Object.entries(wordCounts)) {
    if (!availableLetters[letter] || availableLetters[letter] < count) {
      return false
    }
  }
  return true
}

// Store all results for filtering
let allResults = []

// Display results
function displayResults(results) {
  allResults = results

  // Update length filter options
  const lengths = [...new Set(results.map((w) => w.length))].sort((a, b) => b - a)
  lengthFilter.innerHTML = '<option value="all">All lengths</option>'
  lengths.forEach((len) => {
    lengthFilter.innerHTML += `<option value="${len}">${len} letters</option>`
  })

  filterResults()
}

// Filter and display results
function filterResults() {
  const lengthValue = lengthFilter.value
  const containsValue = containsFilter.value.toLowerCase().trim()

  let filtered = allResults

  if (lengthValue !== "all") {
    filtered = filtered.filter((w) => w.length === Number.parseInt(lengthValue))
  }

  if (containsValue) {
    filtered = filtered.filter((w) => w.includes(containsValue))
  }

  const maxLength = filtered.length > 0 ? Math.max(...filtered.map((w) => w.length)) : 0

  resultsCount.textContent = filtered.length
  resultsGrid.innerHTML = ""

  filtered.forEach((word) => {
    const isLongest = word.length === maxLength
    const card = createWordCard(word, isLongest)
    resultsGrid.appendChild(card)
  })

  if (filtered.length > 0) {
    showResults()
  } else {
    showNoResults()
  }
}

// Create word card element
function createWordCard(word, isLongest) {
  const card = document.createElement("div")
  card.className = `word-card${isLongest ? " longest" : ""}`

  card.innerHTML = `
        <div class="word-info">
            <span class="word-text">${word}</span>
            <span class="word-length">${word.length}</span>
        </div>
        <button class="copy-btn" aria-label="Copy word" data-word="${word}">
            <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
                <rect width="14" height="14" x="8" y="8" rx="2" ry="2"></rect>
                <path d="M4 16c-1.1 0-2-.9-2-2V4c0-1.1.9-2 2-2h10c1.1 0 2 .9 2 2"></path>
            </svg>
        </button>
    `

  const copyBtn = card.querySelector(".copy-btn")
  copyBtn.addEventListener("click", () => copyWord(word, copyBtn))

  return card
}

// Copy single word
async function copyWord(word, btn) {
  try {
    await navigator.clipboard.writeText(word)
    btn.classList.add("copied")
    btn.innerHTML = `
            <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
                <polyline points="20 6 9 17 4 12"></polyline>
            </svg>
        `
    showToast(`Copied "${word}" to clipboard!`)

    setTimeout(() => {
      btn.classList.remove("copied")
      btn.innerHTML = `
                <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
                    <rect width="14" height="14" x="8" y="8" rx="2" ry="2"></rect>
                    <path d="M4 16c-1.1 0-2-.9-2-2V4c0-1.1.9-2 2-2h10c1.1 0 2 .9 2 2"></path>
                </svg>
            `
    }, 2000)
  } catch (err) {
    showToast("Failed to copy")
  }
}

// Copy all words
async function copyAllWords() {
  const lengthValue = lengthFilter.value
  const containsValue = containsFilter.value.toLowerCase().trim()

  let filtered = allResults
  if (lengthValue !== "all") {
    filtered = filtered.filter((w) => w.length === Number.parseInt(lengthValue))
  }
  if (containsValue) {
    filtered = filtered.filter((w) => w.includes(containsValue))
  }

  try {
    await navigator.clipboard.writeText(filtered.join(", "))
    showToast(`Copied ${filtered.length} words to clipboard!`)
  } catch (err) {
    showToast("Failed to copy")
  }
}

// Show/hide states
function showLoading() {
  loading.classList.add("active")
  resultsContainer.classList.remove("active")
  emptyState.classList.add("hidden")
  noResults.classList.add("hidden")
}

function hideLoading() {
  loading.classList.remove("active")
}

function showResults() {
  resultsContainer.classList.add("active")
  emptyState.classList.add("hidden")
  noResults.classList.add("hidden")
}

function showEmptyState() {
  resultsContainer.classList.remove("active")
  emptyState.classList.remove("hidden")
  noResults.classList.add("hidden")
  allResults = []
}

function showNoResults() {
  resultsContainer.classList.remove("active")
  emptyState.classList.add("hidden")
  noResults.classList.remove("hidden")
}

// Toast notification
function showToast(message) {
  toastMessage.textContent = message
  toast.classList.add("active")

  setTimeout(() => {
    toast.classList.remove("active")
  }, 3000)
}

// Theme handling
function initTheme() {
  const savedTheme = localStorage.getItem("theme")
  const prefersDark = window.matchMedia("(prefers-color-scheme: dark)").matches

  if (savedTheme) {
    document.documentElement.setAttribute("data-theme", savedTheme)
  } else if (prefersDark) {
    document.documentElement.setAttribute("data-theme", "dark")
  }
}

function toggleTheme() {
  const currentTheme = document.documentElement.getAttribute("data-theme")
  const newTheme = currentTheme === "dark" ? "light" : "dark"

  document.documentElement.setAttribute("data-theme", newTheme)
  localStorage.setItem("theme", newTheme)
}

// Mobile menu
function toggleMobileMenu() {
  mobileNav.classList.toggle("active")
}

// Subscribe form
function handleSubscribe(e) {
  e.preventDefault()
  const email = e.target.querySelector('input[type="email"]').value
  showToast(`Thanks for subscribing with ${email}!`)
  e.target.reset()
}

